DEGREES dangleclock (DEGREES Direction1, DEGREES Direction2)
/* -------------------------------------------------------------------------------- */
/* dangleclock returns the absolute value (in degrees) of the smaller angle between */
/* two lines on a clock face                                                        */
/* e.g. dangleclock (TWELVE_O_CLOCK, ONE_O_CLOCK)     is  30 degrees                */
/* e.g. dangleclock (TWELVE_O_CLOCK, TWO_O_CLOCK)     is  60 degrees                */
/* e.g. dangleclock (TWELVE_O_CLOCK, THREE_O_CLOCK)   is  90 degrees                */
/* e.g. dangleclock (TWELVE_O_CLOCK, FOUR_O_CLOCK)    is 120 degrees                */
/* e.g. dangleclock (TWELVE_O_CLOCK, FIVE_O_CLOCK)    is 150 degrees                */
/* e.g. dangleclock (TWELVE_O_CLOCK, SIX_O_CLOCK)     is 180 degrees                */
/* e.g. dangleclock (TWELVE_O_CLOCK, SEVEN_O_CLOCK)   is 150 degrees                */
/* e.g. dangleclock (TWELVE_O_CLOCK, EIGHT_O_CLOCK)   is 120 degrees                */
/* e.g. dangleclock (TWELVE_O_CLOCK, NINE_O_CLOCK)    is  90 degrees                */
/* e.g. dangleclock (TWELVE_O_CLOCK, TEN_O_CLOCK)     is  60 degrees                */
/* e.g. dangleclock (TWELVE_O_CLOCK, ELEVEN_O_CLOCK)  is  30 degrees                */
{
	DEGREES dD = Direction1 - Direction2;
	while (dD < 0.0)     dD += 360.0;
	while (dD >= 360.0)  dD -= 360.0;
	return (dD <= 180.0) ? dD : 360.0-dD;
}


DEGREES danglecwise (DEGREES Direction1, DEGREES Direction2)
/* -------------------------------------------------------------------------------- */
/* danglecwise returns the value (in degrees) of the clockwise angle between        */
/* two lines on a clock face; it is always positive                                 */
/* e.g. danglecwise (SIX_O_CLOCK, ONE_O_CLOCK)     is 210 degrees                */
/* e.g. danglecwise (SIX_O_CLOCK, TWO_O_CLOCK)     is 240 degrees                */
/* e.g. danglecwise (SIX_O_CLOCK, THREE_O_CLOCK)   is 270 degrees                */
/* e.g. danglecwise (SIX_O_CLOCK, FOUR_O_CLOCK)    is 300 degrees                */
/* e.g. danglecwise (SIX_O_CLOCK, FIVE_O_CLOCK)    is 310 degrees                */
/* e.g. danglecwise (SIX_O_CLOCK, SIX_O_CLOCK)     is   0 degrees                */
/* e.g. danglecwise (SIX_O_CLOCK, SEVEN_O_CLOCK)   is  30 degrees                */
/* e.g. danglecwise (SIX_O_CLOCK, EIGHT_O_CLOCK)   is  60 degrees                */
/* e.g. danglecwise (SIX_O_CLOCK, NINE_O_CLOCK)    is  90 degrees                */
/* e.g. danglecwise (SIX_O_CLOCK, TEN_O_CLOCK)     is 120 degrees                */
/* e.g. danglecwise (SIX_O_CLOCK, ELEVEN_O_CLOCK)  is 150 degrees                */
{
	while (Direction2 < Direction1)  Direction2 += 360.0;
	return Direction2-Direction1;
}


bool clockwise (DEGREES A, DEGREES B)
/* -------------------------------------------------------------------------------- */
/* clockwise returns whether or not the second direction is within the 180-degree   */
/* arc generated by moving clockwise from the first direction                       */
/* e.g. clockwise (TWELVE_O_CLOCK, TWELVE_O_CLOCK)  is  unreliable                  */
/* e.g. clockwise (TWELVE_O_CLOCK, ONE_O_CLOCK)     is  TRUE                        */
/* e.g. clockwise (TWELVE_O_CLOCK, TWO_O_CLOCK)     is  TRUE                        */
/* e.g. clockwise (TWELVE_O_CLOCK, THREE_O_CLOCK)   is  TRUE                        */
/* e.g. clockwise (TWELVE_O_CLOCK, FOUR_O_CLOCK)    is  TRUE                        */
/* e.g. clockwise (TWELVE_O_CLOCK, FIVE_O_CLOCK)    is  TRUE                        */
/* e.g. clockwise (TWELVE_O_CLOCK, SIX_O_CLOCK)     is  unreliable                  */
/* e.g. clockwise (TWELVE_O_CLOCK, SEVEN_O_CLOCK)   is  FALSE                       */
/* e.g. clockwise (TWELVE_O_CLOCK, EIGHT_O_CLOCK)   is  FALSE                       */
/* e.g. clockwise (TWELVE_O_CLOCK, NINE_O_CLOCK)    is  FALSE                       */
/* e.g. clockwise (TWELVE_O_CLOCK, TEN_O_CLOCK)     is  FALSE                       */
/* e.g. clockwise (TWELVE_O_CLOCK, ELEVEN_O_CLOCK)  is  FALSE                       */
{
	DEGREES C = B-A;
	while (C < 0.0)     C += 360.0;
	while (C >= 360.0)  C -= 360.0;
	return C < 180.0;
}
