void Normalise (VECTOR3D *Vector)
{
	double X = Vector->x;
	double Y = Vector->y;
	double Z = Vector->z;
	double D = sqrt(X*X+Y*Y+Z*Z);
	Vector->x /= D;
	Vector->y /= D;
	Vector->z /= D;
}
